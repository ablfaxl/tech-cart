async function seed() {
  await sequelize.sync({ force: true });

  for (const categoryData of seedData.categories) {
    const category = await Category.create({
      id: categoryData.id,
      name: categoryData.name,
    });

    await Featured.bulkCreate(
      categoryData.featured.map((f) => ({ ...f, CategoryId: category.id }))
    );

    for (const sectionData of categoryData.sections) {
      const section = await Section.create({
        name: sectionData.name,
        CategoryId: category.id,
      });

      await SectionItem.bulkCreate(
        sectionData.items.map((item) => ({ ...item, SectionId: section.id }))
      );
    }
  }
}

seed()
  .then(() => console.log('Database seeded successfully'))
  .catch((error) => console.error('Seeding failed:', error))
  .finally(() => process.exit());
